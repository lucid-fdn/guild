import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { compileFromFile } from "json-schema-to-typescript";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const specDir = path.join(root, "spec");
const outputPath = path.join(root, "sdk/typescript/src/spec.ts");
const checkMode = process.argv.includes("--check");

const schemas = [
  ["Taskpack", "spec/taskpack.schema.json"],
  ["DriBinding", "spec/dri-binding.schema.json"],
  ["Artifact", "spec/artifact.schema.json"],
  ["ApprovalRequest", "spec/approval-request.schema.json"],
  ["PromotionRecord", "spec/promotion-record.schema.json"],
  ["ReplayBundle", "spec/replay-bundle.schema.json"],
  ["WorkspaceConstitution", "spec/workspace-constitution.schema.json"],
  ["ContextPack", "spec/context-pack.schema.json"],
  ["PreflightDecision", "spec/preflight-decision.schema.json"],
];

const header = `/* eslint-disable */\n// Generated by scripts/generate-typescript-spec.mjs. Do not edit by hand.\n\n`;
const chunks = [];

for (const [typeName, schemaPath] of schemas) {
  const compiled = await compileFromFile(path.join(root, schemaPath), {
    bannerComment: "",
    cwd: specDir,
    declareExternallyReferenced: true,
    enableConstEnums: false,
    format: true,
    style: {
      semi: true,
      singleQuote: false,
      tabWidth: 2,
      trailingComma: "none",
      useTabs: false,
    },
    unreachableDefinitions: false,
  });

  chunks.push(compiled.replace(/^export interface \w+/, `export interface ${typeName}`));
}

const output = `${header}${dedupeDeclarations(chunks.join("\n"))}\n`;

if (checkMode) {
  const current = await readFile(outputPath, "utf8");
  if (current !== output) {
    throw new Error("sdk/typescript/src/spec.ts is out of date. Run `make generate-typescript-spec`.");
  }
  console.log("ok sdk/typescript/src/spec.ts is up to date");
} else {
  await writeFile(outputPath, output);
  console.log("generated sdk/typescript/src/spec.ts");
}

function dedupeDeclarations(source) {
  const lines = source.split("\n");
  const output = [];
  const seen = new Set();

  for (let i = 0; i < lines.length; i += 1) {
    const line = lines[i];
    const singleLineDeclaration = line.match(/^export type (\w+) = /);
    if (singleLineDeclaration) {
      const name = singleLineDeclaration[1];
      if (!seen.has(name)) {
        seen.add(name);
        output.push(line);
      }
      continue;
    }

    const interfaceDeclaration = line.match(/^export interface (\w+) /);
    if (!interfaceDeclaration) {
      output.push(line);
      continue;
    }

    const name = interfaceDeclaration[1];
    const block = [line];
    let depth = braceDelta(line);

    while (depth > 0 && i + 1 < lines.length) {
      i += 1;
      block.push(lines[i]);
      depth += braceDelta(lines[i]);
    }

    if (!seen.has(name)) {
      seen.add(name);
      output.push(...block);
    }
  }

  return output.join("\n").replace(/\n{3,}/g, "\n\n").trim();
}

function braceDelta(line) {
  return (line.match(/{/g) ?? []).length - (line.match(/}/g) ?? []).length;
}
